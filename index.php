<b>Офтальмологи провели сенсационную операцию</b>
<br><br>
Жительница Самарской области Нина Трошина стала здоровым человеком. У нее почти 100-процентное зрение, а еще вчера было минус 13. Утром Нина Александровна испытала на себе действие новых технологий — в областной больнице имени Ерошевского провели операцию-презентацию. За работой профессора Золотарёва — он автор методики — наблюдали десятки офтальмологов из Москвы и дальнего зарубежья.
<br>
<br>С подробностями — <b>корреспондент НТВ Михаил Чернов</b>.
<br>
<br>Перед операцией Нине Трошиной в очередной раз проверяют зрение. В начале весны медсестре из Новокуйбышевска попали в глаз снежком. После этого зрение стало стремительно ухудшаться. Нина Трошина, пациентка: «Перед глазами пелена, туман. Нормально видящему человеку зрение минус 13 трудно представить. Это как через грязное стекло смотреть на мир».
<br>
<br>Ее операция как премьера. Врачи шутят: дают живую хирургию. В зрительном зале светила отечественной и зарубежной офтальмологии. Главный врач глазной больницы имени Ерошевского в режиме онлайн демонстрирует уникальную методику удаления катаракты, помутнение хрусталика. Своеобразную будку киномеханика оборудовали прямо в операционной.
<br>
<br>Одна камера снимает общий вид операционной, другую вмонтировали в микроскоп. Это чтобы зрители видели операцию глазами хирурга. Нина Трошина стать звездой согласилась сразу. Операцию ей сделали бесплатно, хотя стоит она 30 тысяч рублей, годовая зарплата медсестры.
<br>
<br>Наглядная демонстрация результатов в прямом эфире. Пациентка сама встает с операционного стола и пересаживается в кресло-каталку. Доктор из Индии Урун Кумар на экран так и смотрел с открытым ртом. Урун Кумар, профессор: «Отличный хирург. Я с ним познакомился еще в Америке. И от его методики я в восторге».
